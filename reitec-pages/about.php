            <h6 class="font-weight-bold text-prmy">Pourquoi choisir le Réseau REITEC Info ?</h5>
            <p>
                REITEC Info est un organisme de formation qui dispense des formations courtes (2 à 5 jours), dont beaucoup certifiâtes. Nous dispensons des formations,
                dans tous les domaines intéressant les Organisation : TIC, Ressources Humaines, Management, Gestion de Projet, Sécurité humanitaire.
            </p>
            <h6 class="font-weight-bold text-prmy">Découvrez 6 bonnes raisons pour choisir les formations de REITEC Info.</h5>
            <p>
                <b class="my-2">1. La Compétence</b><br>
                La formation professionnelle est notre métier depuis 6 ans. Nous dispensons des formations actualisées en permanence afin de répondre au mieux aux besoins ciblés des Organisations. <br>
                <b>2. La Qualification</b> <br>
                Toutes nos formations sont dispensées par des intervenants indépendants et expérimentés, 
                reconnus sur le plan national et international pour leur expertise et leurs compétences pédagogiques. 
                Beaucoup d’entre eux sont les auteurs d’ouvrages de référence dans leur domaine. <br>
                <b>3. L’Expertise Informatique</b> <br>
                Nous sommes leader dans les formations Informatiques avancés, proposée et tenues par des formateurs certifies par MICROSOFT. <br>
                En complément de nos formations sur les NTIC, nous faisons aussi à la demande des prestations 
                d’ingénierie informatique : maintenance, conseil et expertise… avec les meilleurs experts. <br>
                <b>4. Des contributions agréables à nos prestations </b> <br>
                Nous proposons des formations de qualité à caractère internationale à des coûts très concurrentiels, 
                pour permettre aux Organisations humanitaires à différents niveau (international, nationales, locales) et les particuliers d’accéder à nos sessions, <br>
                Des propositions de contributions mensualisées sont mises à la disposition de nos partenaires réguliers avec lesquels nous organisons régulièrement des formations sans cout. <br>
                Ceci est une contribution et soutient aux activités menées par les partenaires auprès de la population affectée par un conflit. <br>
                <b>5. L'accès</b> <br>
                A travers les membres du Réseau REITEC Info, nous organisons des sessions de formation dans les pays du Grand Lacs, 
                avec des formateurs itinérants disponibles à se déplacer vers les bénéficiaires. Ainsi les sessions sont tenues dans les installations du bénéficiaire. <br>
                <b>6. Nos Service</b> <br>
                Vous bénéficiez d’un cadre de formation confortable, entièrement équipé. Jamais plus de 10 participants pour 1 formateur dans une session, 
                pour maintenir la qualité et le niveau d’assimilation de nos cours.
            </p>
        </div>
        <div class="row">

            <div class="col-lg-12 col-md-6 ">
                Le Réseau REITEC Info a déjà réalisé plusieurs sessions de formations, seminaires, ateliers et conférences dans diveses domaines:
                Informatique, Administration et Ressources Humaines, Management, Leadership, Gestion de projet, Finance, Sécurité Humanitaire, Logistique, .
                Ainsi le Réseau REITEC Info propose aux Organisations humanitaires Un service de proximité, ce service couvre un paquet des formations de qualité et personnalisées selon le besoin exprimé pour mieux répondre aux attentes de nos partenaires.
                Nos sessions peuvent se passer sous deux formes :
                <p class="mt-2 p-1 "> <span class="fa fa-chevron-right"></span> <b>1. INTRA – entreprise : Chez un partenaire</b></p>
                Cette formule permet de proposer une formation «sur mesure», qui correspond exactement aux besoins de partenaire. C’est une meilleure formule pour renforcer et homogénéiser les compétences au sein d’une Organisation.
                <p class="mt-2"> <span class="fa fa-chevron-right"></span> <b>2. INTER(EXTRA) – entreprise : Dans les locaux de Reitec Info</b> </p>
                Nous organisons une à deux sessions INTER de formation par mois, au niveau nationale accessibles par tous a travers le pays.
